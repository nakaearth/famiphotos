class Album < ActiveRecord::Base
  include IdEncryptable
  include Searchable

  use_switch_point :famiphoto

  ####################################################################
  ################# Association ######################################
  ####################################################################
  has_many :photos
  belongs_to :group

  ####################################################################
  ####################### Validation #################################
  ####################################################################
  validates :title, presence: true, length: { maximum: 80 }

  ####################################################################
  ##################### コールバック群 ###############################
  ####################################################################

end
